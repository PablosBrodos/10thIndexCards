local urlToModel = {}
local mainName = "Universal 10th index card"

function onLoad()
    setOwner("")
    urlToModel = mergeTables(blacktemplars, imperialknights, astra, worldeaters, darkangels, daemons, tyranids, sm, orks, chaossm, bloodangels, custodes, mechanicus, greyknights, genestealers, deathguard, chaosknights, drukhari, aeldari, sisters, necrons)
    setImageToMatt({face = indexPagesUrl .. "/title.jpg", back = indexPagesUrl .. "/title.jpg"})
end

local exclusionWords = { "of", "in", "the" }

local currentMatches = {}
local objToKey = {}

function mergeTables(...)
    local mergedTable = {}

    for _, tbl in ipairs({ ... }) do
        for key, value in pairs(tbl) do
            mergedTable[key] = value
        end
    end

    return mergedTable
end

function setOwner(owner)
    if owner == "" then
        self.highlightOff()
    else
        self.highlightOn(Color.fromString(owner))
    end

    updateObjectName(owner)
end

function updateObjectName(owner)
    if owner == "" then
        self.setName(mainName)
    else
        local newName = "[" .. owner .. "] " .. mainName
        self.setName(newName)
    end

    self.setVar("owner", owner)
end

function onNumberTyped(player_color, number)
    if number == 6 then
        setOwner("")
    elseif number == 4 then
        setOwner("Blue")
    elseif number == 5 then
        setOwner("Red")
    elseif number == 1 then
        currentMatches.idx = currentMatches.idx - 1
        local elements = currentMatches.elements
        local value = elements[currentMatches.idx]
        if value == nil then
            currentMatches.idx = #elements
            value = elements[currentMatches.idx]
        end
        setImageToMatt({ face = value.properties.face, back = value.properties.back })
    elseif number == 2 then
        currentMatches.idx = currentMatches.idx + 1
        local elements = currentMatches.elements
        local value = elements[currentMatches.idx]
        if value == nil then
            currentMatches.idx = 1
            value = elements[currentMatches.idx]
        end
        setImageToMatt({ face = value.properties.face, back = value.properties.back })
    end

    return true
end

function onPlayerAction(player, action, targets)
    if action == Player.Action.Randomize and (player.color == self.getVar("owner") or self.getVar("owner") == "") then
        local target
        for _, value in pairs(targets) do
            target = value
            break
        end

        local targetName = target.getName()
        local imageUrls = searchTableForKey(urlToModel, targetName)
        if imageUrls == nil or imageUrls.face == "" then
            print(string.format("I couldn't find image for %s", targetName))
            return true
        end
        setImageToMatt(imageUrls)
        return false
    end
end

function setImageToMatt(imageURLs)
    local customObject = self.getCustomObject()
    customObject.face = imageURLs.face
    customObject.back = imageURLs.back
    self.setCustomObject(customObject)
end

-- Function to check if an element already exists in the list
function isElementInList(list, element)
    for _, value in ipairs(list) do
        if value.name == element.name then
            return true
        end
    end
    return false
end

-- Function to add an element to the list if it doesn't already exist
function addToListIfNotExists(list, element)
    if not isElementInList(list, element) then
        table.insert(list, element)
    end
end

local function splitStringIntoWords(str)
    local words = {}
    for word in str:gmatch("%w+") do
        local excludedValue = false
        for _, value in ipairs(exclusionWords) do
            if value == word then
                excludedValue = true
                break
            end
        end
        if not excludedValue then
            table.insert(words, word)
        end
    end
    return words
end

local function findCommonWords(objWords, keyWords)
    local commonWords = {}
    for _, objWord in ipairs(objWords) do
        for _, keyWord in ipairs(keyWords) do
            if objWord == keyWord then
                table.insert(commonWords, objWord)
                break
            end
        end
    end
    return commonWords
end

function searchTableForKey(allTable, objectString)


    local lowercaseObjectString = string.lower(objectString)
    local objWords = splitStringIntoWords(lowercaseObjectString)

    local cachedObject = objToKey[lowercaseObjectString]
    if cachedObject then
        currentMatches = cachedObject
        local value = currentMatches.elements[cachedObject.idx]
        return { face = value.properties.face, back = value.properties.back }
    end

    local matches = {}

    -- Iterate over the allTable and search for a key containing the search string
    for key, value in pairs(allTable) do
        local lowercaseKey = string.lower(key)
        local keyWords = splitStringIntoWords(lowercaseKey)
        local commonWords = findCommonWords(objWords, keyWords)

        local commonWordsSize = #commonWords
        -- Print the common words
        if commonWordsSize > 0 then
            local elem = { name = key, commonNum = commonWordsSize, properties = value,  keyWordSize = #keyWords }
            table.insert(matches, elem)
        end
    end

    local matchesSize = 0
    --if matches then
    for _, _ in pairs(matches) do
        matchesSize = matchesSize + 1
    end

    if matchesSize ~= 0 then
        if matchesSize > 1 then
            print("Multiple matches found: " .. matchesSize)
        end

        table.sort(matches, function(a,b)
            return (a.keyWordSize - a.commonNum) < (b.keyWordSize - b.commonNum)
        end)

        local bestVal = matches[1]
        selectedElem = { face = bestVal.properties.face, back = bestVal.properties.back }
        objToKey[lowercaseObjectString] = { elements = matches, idx = 1 }
        currentMatches = objToKey[lowercaseObjectString]
        return selectedElem
    end

    return nil
end